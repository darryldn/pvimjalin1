    public static RestTicketDto getData(FbTicketDto obj) {
        RestTicketDto ticket = new RestTicketDto();
        ticket.setETA(obj.ETA);
        ticket.setAssignTime(obj.assignTime);
        ticket.setAssignee(obj.assignee);
        ticket.setAssigneeGroup(obj.assigneeGroup);
        ticket.setCategoryName(obj.categoryName);
        ticket.setCloseTime(obj.closeTime);
        ticket.setCreateTime(obj.createTime);
        ticket.setDescription(obj.description);
        ticket.setDispatchTime(obj.dispatchTime);
        ticket.setExpectedFixTime(obj.expectedFixTime);
        ticket.setExpectedResponseTime(obj.expectedResponseTime);
        ticket.setFixTime(obj.fixTime);
        ticket.setIncidentName(obj.incidentName);
        ticket.setMachineNumber(obj.machineNumber);
        ticket.setNote(obj.note);
        ticket.setPriority(obj.priority);
        ticket.setRefId(obj.refId);
        ticket.setReportedTime(obj.reportedTime);
        ticket.setStartWorkingTime(obj.startWorkingTime);
        ticket.setSummary(obj.summary);
        ticket.setSuspendBeginTime(obj.suspendBeginTime);
        ticket.setSuspendEndTime(obj.suspendEndTime);
        ticket.setTicketNumber(obj.ticketNumber);
        ticket.setTicketState(obj.ticketState);
        return ticket;
    }
    
